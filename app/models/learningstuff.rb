class Learningstuff

	attr_accessor :some_stuff, :some_numbers, :some_words

	def initialize()
		:some_stuff
		:some_numbers
		:some_words
	end

	def set_stuff(stuff)
		some_stuff = stuff
	end

	def set_numbers(numbers)
		some_numbers = numbers
	end

	def set_words(words)
		some_words = words
	end


end
